
	<h3>React in case of an earthquake</h3>

<div class="panel-group" id="accordion" role="tablist" aria-multiselectable="true">
  <div class="panel panel-default">
    <div class="panel-heading" role="tab" id="headingOne">
      <h4 class="panel-title">
        <a role="button" data-toggle="collapse" data-parent="#accordion" href="#collapseOne" aria-expanded="true" aria-controls="collapseOne">
          Before an earthquake
        </a>
      </h4>
    </div>
    <div id="collapseOne" class="panel-collapse collapse" role="tabpanel" aria-labelledby="headingOne">
      <div class="panel-body">
        - Get Information : you can ask your municipality about the risks<br>
        - Know the spot to cut your gas and / or electricity income<br>
        - Secure heavy devices or furniture<br>
        - Organise your family to know how to react in case of an earthquake
      </div>
    </div>
  </div>
  <div class="panel panel-default">
    <div class="panel-heading" role="tab" id="headingTwo">
      <h4 class="panel-title">
        <a class="collapsed" role="button" data-toggle="collapse" data-parent="#accordion" href="#collapseTwo" aria-expanded="false" aria-controls="collapseTwo">
          During an earthquake
        </a>
      </h4>
    </div>
    <div id="collapseTwo" class="panel-collapse collapse" role="tabpanel" aria-labelledby="headingTwo">
      <div class="panel-body">
        - Listen to a local radio<br>
        - Stay where you are : <br>
        &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;- If you are inside : go next to a bearing wall or a table<br>
        &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;- If you are outside : do not stay under eletric cables or everything that can collapse<br>
        &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;- In a car : stop driving and wait the end<br>
        - Tell other people around about the correct way to react<br>
        - Protect your head with your arms<br>
        - Do not start any fire<br>

      </div>
    </div>
  </div>
  <div class="panel panel-default">
    <div class="panel-heading" role="tab" id="headingThree">
      <h4 class="panel-title">
        <a class="collapsed" role="button" data-toggle="collapse" data-parent="#accordion" href="#collapseThree" aria-expanded="false" aria-controls="collapseThree">
          After an earthquake
        </a>
      </h4>
    </div>
    <div id="collapseThree" class="panel-collapse collapse" role="tabpanel" aria-labelledby="headingThree">
      <div class="panel-body">
        - Listen to the radio<br>
        - Inform authorities about dangers you can notice<br>
        - Help people around<br>
        - Do not call if it's not necessary<br>
        - Stay away of coastals zones<br>
        - Do not use elevators<br>
        - Stay aware because other earthquakes can follow the first one
      </div>
    </div>
  </div>
